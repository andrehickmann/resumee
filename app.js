const { createApp, nextTick } = Vue;

createApp({
  data() {
    return {
      currentLang: 'de',
      selectedTags: [],
      selectedYears: [],
      paletteOpen: false,
      paletteQuery: '',
      showServiceArrows: false,
      serviceAutoTimer: null,
      contactForm: {
        name: '',
        email: '',
        role: '',
        message: '',
      },
      contactModalOpen: false,
      projectModalOpen: false,
      activeProject: null,
    };
  },
  computed: {
    copy() {
      return content[this.currentLang];
    },
    industryCount() {
      return this.copy.industries.length;
    },
    projectCount() {
      return this.copy.projects.length;
    },
    filteredCount() {
      return this.filteredProjects.length;
    },
    activeFiltersCount() {
      return this.selectedTags.length + this.selectedYears.length;
    },
    projectYears() {
      const years = new Set();
      this.copy.projects.forEach((project) => {
        const start = project.start ?? project.end;
        const end = project.end ?? project.start;
        if (!start || !end) return;
        for (let year = start; year <= end; year += 1) {
          years.add(year);
        }
      });
      return Array.from(years).sort((a, b) => b - a);
    },
    statsDisplay() {
      return this.copy.stats.map((stat) => {
        if (stat.key === 'industries') {
          return { ...stat, value: String(this.industryCount) };
        }
        if (stat.key === 'projects') {
          return { ...stat, value: String(this.projectCount) };
        }
        return stat;
      });
    },
    projectTags() {
      const tags = new Set(this.copy.projects.flatMap((item) => item.tags));
      return [this.copy.filtersAll, ...tags];
    },
    filteredProjects() {
      const list =
        this.selectedTags.length === 0
          ? this.copy.projects
          : this.copy.projects.filter((project) =>
              this.selectedTags.some((tag) => project.tags.includes(tag))
            );
      const yearFiltered =
        this.selectedYears.length === 0
          ? list
          : list.filter((project) => {
              const start = project.start ?? project.end;
              const end = project.end ?? project.start;
              return this.selectedYears.some(
                (year) => year >= start && year <= end
              );
            });
      return [...yearFiltered].sort((a, b) => {
        const endDiff = (b.end ?? b.start) - (a.end ?? a.start);
        if (endDiff !== 0) return endDiff;
        return (b.start ?? 0) - (a.start ?? 0);
      });
    },
    paletteItems() {
      const term = this.paletteQuery.trim().toLowerCase();
      const sectionItems = this.copy.nav.map((item) => ({
        key: `section-${item.id}`,
        label: item.label,
        type: 'Section',
        action: () => this.scrollTo(`#${item.id}`),
      }));
      const filterItems = this.projectTags.map((tag) => ({
        key: `filter-${tag}`,
        label: tag,
        type: 'Filter',
        action: () => this.toggleFilter(tag),
      }));
      const yearItems = this.projectYears.map((year) => ({
        key: `year-${year}`,
        label: String(year),
        type: 'Year',
        action: () => this.toggleYear(year),
      }));
      const items = [...sectionItems, ...filterItems, ...yearItems];
      if (!term) return items;
      return items.filter((item) => item.label.toLowerCase().includes(term));
    },
  },
  watch: {
    currentLang() {
      this.selectedTags = [];
      this.selectedYears = [];
      document.documentElement.lang = this.currentLang;
      document.title = this.copy.pageTitle;
      nextTick(() => {
        this.initReveal();
        this.initScrollEffects();
        this.initCounters();
        this.updateServiceControls();
      });
    },
  },
  mounted() {
    document.documentElement.lang = this.currentLang;
    document.title = this.copy.pageTitle;
    this.initScroll();
    this.initNavHighlight();
    this.initReveal();
    this.initScrollEffects();
    this.initCounters();
    this.initCommandPalette();
    this.initMagnet();
    this.initTilt();
    this.initServiceSlider();
    window.addEventListener('keydown', this.handleKeydown);
  },
  methods: {
    setLang(lang) {
      this.currentLang = lang;
    },
    toggleFilter(tag) {
      if (tag === this.copy.filtersAll) {
        this.selectedTags = [];
        return;
      }
      if (this.selectedTags.includes(tag)) {
        this.selectedTags = this.selectedTags.filter((item) => item !== tag);
      } else {
        this.selectedTags = [...this.selectedTags, tag];
      }
      nextTick(() => this.initReveal());
    },
    toggleYear(year) {
      if (this.selectedYears.includes(year)) {
        this.selectedYears = this.selectedYears.filter((item) => item !== year);
      } else {
        this.selectedYears = [...this.selectedYears, year];
      }
      nextTick(() => this.initReveal());
    },
    isTagActive(tag) {
      if (tag === this.copy.filtersAll) {
        return this.selectedTags.length === 0;
      }
      return this.selectedTags.includes(tag);
    },
    isYearActive(year) {
      return this.selectedYears.includes(year);
    },
    resetFilters() {
      this.selectedTags = [];
      this.selectedYears = [];
      nextTick(() => this.initReveal());
    },
    submitContact() {
      const { name, email, role, message } = this.contactForm;
      const subject = encodeURIComponent('Festanstellung · Kontaktaufnahme');
      const body = encodeURIComponent(
        `Name: ${name}\nE-Mail: ${email}\nRolle: ${role}\n\nNachricht:\n${message}`
      );
      window.location.href = `mailto:andre@hickmann-kuschnereit.de?subject=${subject}&body=${body}`;
    },
    openContactModal() {
      this.contactModalOpen = true;
    },
    closeContactModal() {
      this.contactModalOpen = false;
    },
    openProject(project) {
      this.activeProject = project;
      this.projectModalOpen = true;
    },
    closeProjectModal() {
      this.projectModalOpen = false;
      this.activeProject = null;
    },
    handleKeydown(event) {
      if (event.key === 'Escape') {
        if (this.contactModalOpen) this.closeContactModal();
        if (this.projectModalOpen) this.closeProjectModal();
      }
    },
    scrollServices(direction) {
      const track = this.$refs.servicesTrack;
      if (!track) return;
      const step = Math.max(track.clientWidth * 0.7, 240);
      track.scrollBy({ left: direction * step, behavior: 'smooth' });
    },
    updateServiceControls() {
      const track = this.$refs.servicesTrack;
      if (!track) return;
      this.showServiceArrows = track.scrollWidth > track.clientWidth + 4;
    },
    initServiceSlider() {
      const track = this.$refs.servicesTrack;
      if (!track) return;
      let isDown = false;
      let startX = 0;
      let scrollLeft = 0;
      const setDragging = (value) => {
        track.classList.toggle('dragging', value);
      };
      const stopAuto = () => {
        if (this.serviceAutoTimer) {
          clearInterval(this.serviceAutoTimer);
          this.serviceAutoTimer = null;
        }
      };
      const startAuto = () => {
        if (this.serviceAutoTimer) return;
        this.serviceAutoTimer = setInterval(() => {
          if (!track) return;
          const maxScroll = track.scrollWidth - track.clientWidth;
          if (maxScroll <= 0) return;
          const next = track.scrollLeft + track.clientWidth * 0.6;
          track.scrollTo({
            left: next >= maxScroll ? 0 : next,
            behavior: 'smooth',
          });
        }, 2800);
      };
      track.addEventListener('mousedown', (event) => {
        isDown = true;
        startX = event.pageX - track.offsetLeft;
        scrollLeft = track.scrollLeft;
        setDragging(true);
        stopAuto();
        event.preventDefault();
      });
      track.addEventListener('mousemove', (event) => {
        if (!isDown) return;
        const x = event.pageX - track.offsetLeft;
        const walk = (x - startX) * 1.2;
        track.scrollLeft = scrollLeft - walk;
      });
      const stopDrag = () => {
        isDown = false;
        setDragging(false);
        startAuto();
      };
      track.addEventListener('mouseleave', stopDrag);
      track.addEventListener('mouseup', stopDrag);
      track.addEventListener('mouseenter', stopAuto);
      track.addEventListener('touchstart', stopAuto, { passive: true });
      track.addEventListener('touchend', startAuto, { passive: true });
      window.addEventListener('resize', this.updateServiceControls, { passive: true });
      nextTick(() => this.updateServiceControls());
      startAuto();
    },
    scrollTo(selector) {
      const target = document.querySelector(selector);
      if (target) {
        target.scrollIntoView({ behavior: 'smooth' });
      }
    },
    initScroll() {
      document.querySelectorAll('[data-scroll]').forEach((button) => {
        button.addEventListener('click', () => {
          const target = document.querySelector(button.dataset.scroll);
          if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
          }
        });
      });
    },
    initNavHighlight() {
      const sections = document.querySelectorAll('main section');
      const navLinks = document.querySelectorAll('.nav a');
      const observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              navLinks.forEach((link) => {
                link.classList.toggle(
                  'active',
                  link.getAttribute('href') === `#${entry.target.id}`
                );
              });
            }
          });
        },
        { threshold: 0.5 }
      );

      sections.forEach((section) => observer.observe(section));
    },
    initReveal() {
      const revealItems = document.querySelectorAll('.reveal');
      const observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              entry.target.classList.add('visible');
              observer.unobserve(entry.target);
            }
          });
        },
        { threshold: 0.15 }
      );

      revealItems.forEach((item) => observer.observe(item));
    },
    initScrollEffects() {
      const portraitWrap = document.querySelector('.portrait-wrap');
      const blur = document.querySelector('.bg-blur');
      if (!portraitWrap || !blur) return;
      let ticking = false;
      const update = () => {
        const offset = Math.min(window.scrollY, 800);
        portraitWrap.style.setProperty('--scroll-shift', `${offset * 0.08}px`);
        blur.style.transform = `translateY(${offset * -0.05}px)`;
        ticking = false;
      };
      window.addEventListener(
        'scroll',
        () => {
          if (!ticking) {
            window.requestAnimationFrame(update);
            ticking = true;
          }
        },
        { passive: true }
      );
      update();
    },
    initCounters() {
      const counters = document.querySelectorAll('[data-count]');
      const prefersReduce = window.matchMedia('(prefers-reduced-motion: reduce)');
      if (prefersReduce.matches) {
        counters.forEach((item) => {
          item.textContent = item.dataset.count;
        });
        return;
      }
      const parseValue = (value) => {
        const numeric = parseInt(value.replace(/\D/g, ''), 10);
        return Number.isNaN(numeric) ? 0 : numeric;
      };
      const observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (!entry.isIntersecting) return;
            const target = entry.target;
            const value = target.dataset.count;
            const number = parseValue(value);
            const hasPlus = value.includes('+');
            let current = 0;
            const duration = 1100;
            const start = performance.now();
            const tick = (now) => {
              const progress = Math.min((now - start) / duration, 1);
              current = Math.floor(number * progress);
              target.textContent = `${current}${hasPlus ? '+' : ''}`;
              if (progress < 1) {
                requestAnimationFrame(tick);
              } else {
                target.textContent = value;
              }
            };
            requestAnimationFrame(tick);
            observer.unobserve(target);
          });
        },
        { threshold: 0.7 }
      );
      counters.forEach((item) => observer.observe(item));
    },
    initCommandPalette() {
      window.addEventListener('keydown', (event) => {
        if ((event.ctrlKey || event.metaKey) && event.key.toLowerCase() === 'k') {
          event.preventDefault();
          this.openPalette();
        }
        if (event.key === 'Escape' && this.paletteOpen) {
          this.closePalette();
        }
      });
    },
    openPalette() {
      this.paletteOpen = true;
      this.paletteQuery = '';
      nextTick(() => this.$refs.paletteInput?.focus());
    },
    closePalette() {
      this.paletteOpen = false;
    },
    runPalette(item) {
      item.action();
      this.closePalette();
    },
    initMagnet() {
      const items = document.querySelectorAll('[data-magnet]');
      const prefersReduce = window.matchMedia('(prefers-reduced-motion: reduce)');
      if (prefersReduce.matches) return;
      items.forEach((item) => {
        item.addEventListener('mousemove', (event) => {
          const rect = item.getBoundingClientRect();
          const x = event.clientX - rect.left - rect.width / 2;
          const y = event.clientY - rect.top - rect.height / 2;
          item.style.transform = `translate(${x * 0.08}px, ${y * 0.08}px)`;
        });
        item.addEventListener('mouseleave', () => {
          item.style.transform = 'translate(0px, 0px)';
        });
      });
    },
    initTilt() {
      const cards = document.querySelectorAll('.tilt');
      const prefersReduce = window.matchMedia('(prefers-reduced-motion: reduce)');
      if (prefersReduce.matches) return;
      cards.forEach((card) => {
        card.addEventListener('mousemove', (event) => {
          const rect = card.getBoundingClientRect();
          const x = (event.clientX - rect.left) / rect.width - 0.5;
          const y = (event.clientY - rect.top) / rect.height - 0.5;
          card.style.transform = `perspective(900px) rotateX(${y * -6}deg) rotateY(${x * 6}deg)`;
        });
        card.addEventListener('mouseleave', () => {
          card.style.transform = 'perspective(900px) rotateX(0deg) rotateY(0deg)';
        });
      });
    },
  },
}).mount('#app');
